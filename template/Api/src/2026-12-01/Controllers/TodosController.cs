namespace TodoSample.Api.v2026_12_01.Controllers;

using System.Globalization;
using Mediator;
using Microsoft.AspNetCore.Mvc;
using Trellis;
using Trellis.Asp;
using Trellis.Asp.ApiVersioning;
using Trellis.Asp.Idempotency;
using Trellis.ServiceLevelIndicators;
using TodoSample.Api.v2026_12_01.Models;
using TodoSample.Application.Todos;
using TodoSample.Domain;

/// <summary>
/// Todo items controller.
/// <para>
/// Showcases the HTTP RFC features Trellis implements:
/// </para>
/// <list type="bullet">
///   <item><description>RFC 9110 ETag (strong) — <c>WithETag</c> on read/write paths.</description></item>
///   <item><description>RFC 9110 Last-Modified / If-Modified-Since / If-Unmodified-Since — <c>WithLastModified</c> + <c>EvaluatePreconditions</c> on GETs.</description></item>
///   <item><description>RFC 9110 If-None-Match → <c>304 Not Modified</c> via <c>EvaluatePreconditions</c>.</description></item>
///   <item><description>RFC 9110 If-Match → <c>412 Precondition Failed</c> on stale write via <c>RequireETag</c>.</description></item>
///   <item><description>RFC 6585 <c>428 Precondition Required</c> when <c>If-Match</c> is missing on PUT.</description></item>
///   <item><description>RFC 7240 <c>Prefer: return=minimal | return=representation</c> — <c>HonorPrefer()</c> on PUT.</description></item>
///   <item><description>RFC 9110 unified write semantics — <c>WriteOutcome.Updated</c> on PUT.</description></item>
///   <item><description>RFC 8288 <c>Link</c> header pagination — <c>Result&lt;Page&lt;T&gt;&gt;</c> on listings.</description></item>
///   <item><description>RFC 9457 Problem Details — emitted automatically for any failed <c>Result</c>.</description></item>
/// </list>
/// </summary>
[ApiController]
[Produces("application/json")]
[Route("api/[controller]")]
public class TodosController : ControllerBase
{
    private readonly ISender _sender;
    private readonly TimeProvider _timeProvider;

    /// <summary>
    /// Constructor.
    /// </summary>
    public TodosController(ISender sender, TimeProvider timeProvider)
    {
        _sender = sender;
        _timeProvider = timeProvider;
    }

    /// <summary>
    /// Create a new todo item.
    /// <para>
    /// Returns <c>201 Created</c> with a strongly-typed <c>ETag</c> and <c>Last-Modified</c> header.
    /// The <c>Location</c> header is generated by <c>CreatedAtVersionedRoute</c> (from the
    /// <c>Trellis.Asp.ApiVersioning</c> package) using the named <c>Todos_GetById</c> route. The
    /// helper auto-injects the <c>api-version</c> route value at request time so the emitted
    /// <c>Location</c> URL round-trips the version the client used (RFC 9110 §15.3.2).
    /// </para>
    /// </summary>
    [HttpPost]
    [Consumes("application/json")]
    [Idempotent]
    [ProducesResponseType(typeof(TodoResponse), StatusCodes.Status201Created)]
    [ProducesResponseType(StatusCodes.Status403Forbidden)]
    [ProducesResponseType(StatusCodes.Status422UnprocessableEntity)]
    public ValueTask<ActionResult<TodoResponse>> Create(
        [FromBody] CreateTodoRequest request,
        CancellationToken cancellationToken) =>
        _sender.Send(new CreateTodoCommand(request.Title, request.DueDate, request.Tag), cancellationToken)
            .ToHttpResponseAsync(
                todo => TodoResponse.From(todo, _timeProvider.GetUtcNow().UtcDateTime),
                opts => opts
                    .CreatedAtRoute("Todos_GetById", t => new Microsoft.AspNetCore.Routing.RouteValueDictionary
                    {
                        ["id"] = (Guid)t.Id,
                    })
                    .WithVersionedRoute()
                    .WithETag(t => EntityTagValue.Strong(t.ETag))
                    .WithLastModified(t => t.LastModified))
            .AsActionResultAsync<TodoResponse>();

    /// <summary>
    /// Get a todo item by ID.
    /// <para>
    /// Emits <c>ETag</c> + <c>Last-Modified</c> response headers. <c>EvaluatePreconditions()</c>
    /// honors <c>If-None-Match</c> / <c>If-Modified-Since</c> (returning <c>304 Not Modified</c>)
    /// and <c>If-Match</c> / <c>If-Unmodified-Since</c> (returning <c>412 Precondition Failed</c>).
    /// </para>
    /// </summary>
    [HttpGet("{id}", Name = "Todos_GetById")]
    [ProducesResponseType(typeof(TodoResponse), StatusCodes.Status200OK)]
    [ProducesResponseType(StatusCodes.Status304NotModified)]
    [ProducesResponseType(StatusCodes.Status404NotFound)]
    [ProducesResponseType(StatusCodes.Status412PreconditionFailed)]
    public ValueTask<ActionResult<TodoResponse>> GetById(
        [CustomerResourceId] TodoId id,
        CancellationToken cancellationToken) =>
        _sender.Send(new GetTodoByIdQuery(id), cancellationToken)
            .ToHttpResponseAsync(
                todo => TodoResponse.From(todo, _timeProvider.GetUtcNow().UtcDateTime),
                opts => opts
                    .WithETag(t => EntityTagValue.Strong(t.ETag))
                    .WithLastModified(t => t.LastModified)
                    .EvaluatePreconditions())
            .AsActionResultAsync<TodoResponse>();

    /// <summary>
    /// Get a paginated page of overdue todo items, ordered by <c>Id</c>.
    /// <para>
    /// Returns <see cref="PagedResponse{TResponse}"/> JSON with an RFC 8288 <c>Link</c> header
    /// (<c>rel="next"</c> / <c>rel="prev"</c>) when more pages are available.
    /// A malformed <c>cursor</c> surfaces as <c>422 Unprocessable Content</c>.
    /// </para>
    /// </summary>
    /// <param name="cursor">Opaque continuation token from the previous page's <c>next</c> link.</param>
    /// <param name="limit">Page size requested by the client; the server clamps to its maximum.</param>
    /// <param name="cancellationToken">Cancellation token.</param>
    [HttpGet("overdue")]
    [ProducesResponseType(typeof(PagedResponse<TodoResponse>), StatusCodes.Status200OK)]
    [ProducesResponseType(StatusCodes.Status403Forbidden)]
    [ProducesResponseType(StatusCodes.Status422UnprocessableEntity)]
    public ValueTask<ActionResult<PagedResponse<TodoResponse>>> GetOverdue(
        [FromQuery] string? cursor,
        [FromQuery] int limit,
        CancellationToken cancellationToken) =>
        _sender.Send(new GetOverdueTodosQuery(cursor, limit), cancellationToken)
            .ToHttpResponseAsync(
                nextUrlBuilder: (c, applied) =>
                    $"{Request.Scheme}://{Request.Host}{Request.PathBase}/api/Todos/overdue" +
                    $"?cursor={c.Token}&limit={applied.ToString(CultureInfo.InvariantCulture)}&api-version=2026-12-01",
                body: todo => TodoResponse.From(todo, _timeProvider.GetUtcNow().UtcDateTime))
            .AsActionResultAsync<PagedResponse<TodoResponse>>();

    /// <summary>
    /// Update a todo item's title, due date, and tag.
    /// <para>
    /// Requires <c>If-Match</c> (RFC 6585 — missing → <c>428 Precondition Required</c>;
    /// stale → RFC 9110 <c>412 Precondition Failed</c>).
    /// Honors RFC 7240 <c>Prefer: return=minimal</c> by returning <c>204 No Content</c>
    /// in place of <c>200 OK</c> via <c>HonorPrefer()</c> on the <see cref="WriteOutcome{T}.Updated"/> outcome.
    /// </para>
    /// </summary>
    [HttpPut("{id}")]
    [Consumes("application/json")]
    [ProducesResponseType(typeof(TodoResponse), StatusCodes.Status200OK)]
    [ProducesResponseType(StatusCodes.Status204NoContent)]
    [ProducesResponseType(StatusCodes.Status404NotFound)]
    [ProducesResponseType(StatusCodes.Status412PreconditionFailed)]
    [ProducesResponseType(StatusCodes.Status422UnprocessableEntity)]
    [ProducesResponseType(StatusCodes.Status428PreconditionRequired)]
    public Task<ActionResult<TodoResponse>> Update(
        [CustomerResourceId] TodoId id,
        [FromBody] UpdateTodoRequest request,
        CancellationToken cancellationToken)
    {
        var ifMatchETags = ETagHelper.ParseIfMatch(Request);
        return UpdateTodoCommand.TryCreate(id, request.Title, request.DueDate, request.Tag, ifMatchETags)
            .BindAsync(command => _sender.Send(command, cancellationToken).AsTask())
            .MapAsync(t => (WriteOutcome<TodoItem>)new WriteOutcome<TodoItem>.Updated(
                t,
                Metadata: RepresentationMetadata.Create()
                    .SetStrongETag(t.ETag)
                    .SetLastModified(t.LastModified)
                    .Build()))
            .ToHttpResponseAsync(
                todo => TodoResponse.From(todo, _timeProvider.GetUtcNow().UtcDateTime),
                opts => opts.HonorPrefer())
            .AsActionResultAsync<TodoResponse>();
    }

    /// <summary>
    /// Complete a todo item. Only the creator can complete their own todo.
    /// <para>
    /// Body-less state-transition POST. Does <strong>not</strong> require <c>If-Match</c>:
    /// the state machine guard on the aggregate rejects stale transitions (e.g., completing
    /// an already-completed todo) with <c>422 Unprocessable Content</c>. There is no body to
    /// overwrite, so a precondition header would be ceremony without benefit. See the
    /// template's "Require <c>If-Match</c> on body-overwriting mutations" rule.
    /// </para>
    /// </summary>
    [HttpPost("{id}/complete")]
    [ProducesResponseType(typeof(TodoResponse), StatusCodes.Status200OK)]
    [ProducesResponseType(StatusCodes.Status403Forbidden)]
    [ProducesResponseType(StatusCodes.Status404NotFound)]
    [ProducesResponseType(StatusCodes.Status422UnprocessableEntity)]
    public ValueTask<ActionResult<TodoResponse>> Complete(
        [CustomerResourceId] TodoId id,
        CancellationToken cancellationToken) =>
        _sender.Send(new CompleteTodoCommand(id), cancellationToken)
            .ToHttpResponseAsync(
                todo => TodoResponse.From(todo, _timeProvider.GetUtcNow().UtcDateTime),
                opts => opts
                    .WithETag(t => EntityTagValue.Strong(t.ETag))
                    .WithLastModified(t => t.LastModified))
            .AsActionResultAsync<TodoResponse>();

    /// <summary>
    /// Demonstrates the deterministic <see cref="Error.Unexpected"/> path with a stable
    /// fault identifier the client can quote in support tickets. The Trellis HTTP mapper
    /// emits an RFC 9457 ProblemDetails body with the fault id surfaced as an extension,
    /// without leaking exception details.
    /// </summary>
    [HttpGet("fault")]
    [ApiExplorerSettings(IgnoreApi = true)]
    public Microsoft.AspNetCore.Http.IResult Fault() =>
        new Error.Unexpected("todos_fault", "TODOS-FAULT-001")
        {
            Detail = "Deterministic fault path used to demonstrate Error.Unexpected mapping.",
        }.ToHttpResponse();

    /// <summary>
    /// This method throws to demonstrate that the ASP.NET exception handling pipeline
    /// (UseExceptionHandler + ProblemDetails) wraps unhandled exceptions in an RFC 9457 body
    /// with a correlation trace id. Use <see cref="Fault"/> for the typed
    /// <see cref="Error.Unexpected"/> pattern with a stable fault id.
    /// </summary>
    /// <exception cref="NotImplementedException"></exception>
    [HttpGet("throw")]
    [ApiExplorerSettings(IgnoreApi = true)]
    public string Throw()
    {
        throw new NotImplementedException("Catch me middleware.");
    }

    /// <summary>
    /// Delete a todo item.
    /// <para>
    /// Destructive mutation. Requires <c>If-Match</c> (RFC 6585 — missing →
    /// <c>428 Precondition Required</c>; stale → RFC 9110 <c>412 Precondition Failed</c>)
    /// to prevent lost-update races against concurrent mutations.
    /// </para>
    /// </summary>
    [HttpDelete("{id}")]
    [ProducesResponseType(StatusCodes.Status204NoContent)]
    [ProducesResponseType(StatusCodes.Status404NotFound)]
    [ProducesResponseType(StatusCodes.Status412PreconditionFailed)]
    [ProducesResponseType(StatusCodes.Status428PreconditionRequired)]
    public ValueTask<Microsoft.AspNetCore.Http.IResult> Delete(
        [CustomerResourceId] TodoId id,
        CancellationToken cancellationToken)
    {
        var ifMatchETags = ETagHelper.ParseIfMatch(Request);
        return _sender.Send(new DeleteTodoCommand(id, ifMatchETags), cancellationToken)
            .ToHttpResponseAsync();
    }
}

